def _ensure_reference_data():
    concern_map = {}
    for key, name in CONCERNS:
        concern, _ = SkinConcern.objects.update_or_create(
            key=key,
            defaults={"name": name},
        )
        concern_map[key] = concern

    skin_type_map = {}
    for key, name in SKIN_TYPES:
        skin_type, _ = SkinTypeTag.objects.update_or_create(
            key=key,
            defaults={"name": name},
        )
        skin_type_map[key] = skin_type

    restriction_map = {}
    for key, name in RESTRICTIONS:
        restriction, _ = RestrictionTag.objects.update_or_create(
            key=key,
            defaults={"name": name},
        )
        restriction_map[key] = restriction

    ingredient_map: dict[str, Ingredient] = {}
    for key, common_name, benefits in INGREDIENTS:
        possible_keys = {key}
        if "-" in key:
            possible_keys.add(key.replace("-", "_"))
        if "_" in key:
            possible_keys.add(key.replace("_", "-"))
        ingredient: Ingredient | None = None
        for candidate in possible_keys:
            ingredient = Ingredient.objects.filter(key=candidate).first()
            if ingredient:
                break
        if ingredient:
            updates = {}
            if ingredient.common_name != common_name:
                updates["common_name"] = common_name
            if ingredient.benefits != benefits:
                updates["benefits"] = benefits
            notes = INGREDIENT_NOTES.get(key, {})
            for field, value in notes.items():
                if getattr(ingredient, field) != value:
                    updates[field] = value
            if updates:
                Ingredient.objects.filter(pk=ingredient.pk).update(**updates)
                ingredient.refresh_from_db(fields=list(updates.keys()))
        else:
            notes = INGREDIENT_NOTES.get(key, {})
            ingredient = Ingredient.objects.create(
                key=key,
                common_name=common_name,
                benefits=benefits,
                helps_with=notes.get("helps_with", ""),
                avoid_with=notes.get("avoid_with", ""),
                side_effects=notes.get("side_effects", ""),
            )
        ingredient_map[key] = ingredient

    return concern_map, skin_type_map, restriction_map, ingredient_map


def _clip(value: str | None, max_length: int) -> str:
    if not value:
        return ""
    trimmed = value.strip()
    if len(trimmed) <= max_length:
        return trimmed
    return trimmed[:max_length]


def _normalize_origin(value: str | None) -> str:
    if not value:
        return Product.Origin.OTHER

    raw = value.strip()
    if not raw:
        return Product.Origin.OTHER

    direct = raw.upper()
    valid = {choice.value for choice in Product.Origin}
    if direct in valid:
        return direct

    normalized = direct.replace(" ", "").replace("-", "")
    mapper = {
        "SOUTHKOREA": Product.Origin.SOUTH_KOREA,
        "KOREA": Product.Origin.SOUTH_KOREA,
        "KOR": Product.Origin.SOUTH_KOREA,
        "REPUBLICOFKOREA": Product.Origin.SOUTH_KOREA,
        "JAPAN": Product.Origin.JAPAN,
        "JPN": Product.Origin.JAPAN,
        "USA": Product.Origin.UNITED_STATES,
        "UNITEDSTATES": Product.Origin.UNITED_STATES,
        "UNITEDSTATE": Product.Origin.UNITED_STATES,
        "US": Product.Origin.UNITED_STATES,
        "UK": Product.Origin.UNITED_KINGDOM,
        "UNITEDKINGDOM": Product.Origin.UNITED_KINGDOM,
        "GBR": Product.Origin.UNITED_KINGDOM,
        "ENG": Product.Origin.UNITED_KINGDOM,
        "ENGLAND": Product.Origin.UNITED_KINGDOM,
        "FRANCE": Product.Origin.FRANCE,
        "FRA": Product.Origin.FRANCE,
        "CANADA": Product.Origin.CANADA,
        "CAN": Product.Origin.CANADA,
        "THAILAND": Product.Origin.THAI,
        "THAI": Product.Origin.THAI,
        "CHINA": Product.Origin.CHINA,
        "CHN": Product.Origin.CHINA,
        "CN": Product.Origin.CHINA,
    }
    return mapper.get(normalized, Product.Origin.OTHER)


def _product_defaults(seed: ProductSeed) -> dict:
    return {
        "brand": _clip(seed.brand, Product._meta.get_field("brand").max_length),
        "name": _clip(seed.name, Product._meta.get_field("name").max_length),
        "origin_country": _normalize_origin(seed.origin),
        "category": seed.category,
        "summary": _clip(seed.summary, Product._meta.get_field("summary").max_length),
        "hero_ingredients": _clip(seed.hero_ingredients, Product._meta.get_field("hero_ingredients").max_length),
        "price": Decimal(seed.price),
        "currency": seed.currency,
        "rating": seed.rating,
        "product_url": _clip(seed.product_url, Product._meta.get_field("product_url").max_length),
        "image": seed.image or "",
        "is_active": True,
    }


def _set_product_relations(
    product: Product,
    seed: ProductSeed,
    concern_map: dict[str, SkinConcern],
    skin_type_map: dict[str, SkinTypeTag],
    restriction_map: dict[str, RestrictionTag],
    ingredient_map: dict[str, Ingredient],
) -> None:
    ProductConcern.objects.filter(product=product).delete()
    for index, concern_key in enumerate(seed.concerns):
        if concern_key not in concern_map:
            print(f"Warning: Concern '{concern_key}' not found in CONCERNS for product {product.brand} - {product.name}. Skipping.")
            continue
        concern = concern_map[concern_key]
        ProductConcern.objects.create(
            product=product,
            concern=concern,
            weight=90 - index * 10,
        )

    ProductIngredient.objects.filter(product=product).delete()
    for order, ingredient_key in enumerate(seed.ingredients):
        if ingredient_key not in ingredient_map:
            print(f"Warning: Ingredient '{ingredient_key}' not found in INGREDIENTS for product {product.brand} - {product.name}. Skipping.")
            continue
        ingredient = ingredient_map[ingredient_key]
        ProductIngredient.objects.create(
            product=product,
            ingredient=ingredient,
            order=order,
            highlight=order < 3,
        )

    ProductSkinType.objects.filter(product=product).delete()
    for skin_type_key in seed.skin_types:
        if skin_type_key not in skin_type_map:
            print(f"Warning: Skin type '{skin_type_key}' not found in SKIN_TYPES for product {product.brand} - {product.name}. Skipping.")
            continue
        skin_type = skin_type_map[skin_type_key]
        ProductSkinType.objects.create(
            product=product,
            skin_type=skin_type,
            compatibility=85,
        )

    restriction_objects = []
    for key in seed.restrictions:
        if key not in restriction_map:
            print(f"Warning: Restriction '{key}' not found in RESTRICTIONS for product {product.brand} - {product.name}. Skipping.")
            continue
        restriction_objects.append(restriction_map[key])
    product.restrictions.set(restriction_objects)


class Command(BaseCommand):
    help = "Load a curated catalog of skincare products into the database."

    def add_arguments(self, parser):
        parser.add_argument(
            "--reset",
            action="store_true",
            help="Delete existing catalog entries before loading sample data.",
        )

    @transaction.atomic
    def handle(self, *args, **options):
        from django.apps import apps

        if options["reset"]:
            self.stdout.write("Clearing existing catalog data...")
            ProductSkinType.objects.all().delete()
            ProductIngredient.objects.all().delete()
            ProductConcern.objects.all().delete()
            Product.objects.all().delete()

        concern_map, skin_type_map, restriction_map, ingredient_map = _ensure_reference_data()

        if not PRODUCTS:
            raise RuntimeError("PRODUCTS dataset is empty; populate it before running the command.")

        created, updated = 0, 0
        for seed in PRODUCTS:
            slug = _clip(slugify(f"{seed.brand} {seed.name}"), Product._meta.get_field("slug").max_length)
            defaults = _product_defaults(seed)
            product, is_created = Product.objects.update_or_create(
                slug=slug,
                defaults=defaults,
            )
            _set_product_relations(
                product,
                seed,
                concern_map,
                skin_type_map,
                restriction_map,
                ingredient_map,
            )
            if is_created:
                created += 1
            else:
                updated += 1

        self.stdout.write(self.style.SUCCESS(
            f"Catalog load complete: {created} created, {updated} updated."
        ))
